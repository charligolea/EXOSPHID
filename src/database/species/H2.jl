# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Wavelength Threshold in Angstrom
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const wvl_thr_H2 = 2768.0f0

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Photodestruction Rates for Quiet (qr) and Active (ar) Sun
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const qr_H2 = 14.24f-6
const ar_H2 = 32.83f-6


# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Threshold Energies in eV: Single Float32 for SPD and SPI, 2D Tuple for DPD, DPI, DiPI (1st bond energy, 2nd bond energy)
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const tsh_H2 = (
    4.48f0, # " H2 -> H(1S) + H(1S)"
    14.68f0, # " H2 -> H(2S,2P) + H(2S,2P)"
    15.43f0, # " H2 -> H2(+) + e-"
    (15.43f0, 17.82f0) # " H2 -> H(+) + H + e-" (The first is the ionisation energy, the second is the total dissociative ionisation energy)
)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Species Names 1D Tuple with 3 elements for SPD, SPI, 2D Tuple with 3 elements for DPD, DPI, DiPI
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const sn_H2 = (
    ("H2", "H", "H"), # " H2 -> H(1S) + H(1S)"
    ("H2", "H", "H"), # " H2 -> H(2S,2P) + H(2S,2P)"
    ("H2", "H2(+)", "e(-)"),   # " H2 -> H2(+) + e-"
    (("H2", "H2(+)", "e(-)"), ("H2(+)", "H(+)", "H"))  # " H2 -> H(+) + H + e-" (The first is the ionisation energy, the second is the total dissociative ionisation energy)
)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Reaction probabilities: Every Tuple must contain as many elements as the number of wavelength ranges
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const rp_H2 = (
    (1.00f0, 0.00f0, 0.00f0, 0.00f0), # " H2 -> H(1S) + H(1S)"
    (0.00f0, 1.00f0, 0.00f0, 0.00f0), # " H2 -> H(2S,2P) + H(2S,2P)"
    (0.00f0, 0.00f0, 1.00f0, 0.60f0), # " H2 -> H2(+) + e-"
    (0.00f0, 0.00f0, 0.00f0, 0.40f0)  # " H2 -> H(+) + H + e-" (The first is the ionisation energy, the second is the total dissociative ionisation energy)
)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Reaction Identifiers: Custom, for posterior analysis
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const rn_H2 = (
    "H2-PD1", # " H2 -> H(1S) + H(1S)"
    "H2-PD2", # " H2 -> H(2S,2P) + H(2S,2P)"
    "H2-PI1", # " H2 -> H2(+) + e-"
    "H2-PI2"  # " H2 -> H(+) + H + e-"
)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Reaction Types: SPD / SPI / DPD / DPI / DiPI
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const rt_H2 = (
    "SPD", # " H2 -> H(1S) + H(1S)"
    "SPD", # " H2 -> H(2S,2P) + H(2S,2P)"
    "SPI", # " H2 -> H2(+) + e-"
    "DiPI"  # " H2 -> H(+) + H + e-"
)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Wavelength Ranges
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const wv_H2 = ((844.8f0, 2768.0f0), (803.7f0, 844.8f0), (695.8f0, 803.7f0), (0f0, 695.8f0))

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Build Struct
