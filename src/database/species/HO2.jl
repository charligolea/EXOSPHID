# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Wavelength Threshold in Angstrom
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const wvl_thr_HO2 =  4395.0f0

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Photodestruction Rates for Quiet (qr) and Active (ar) Sun
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const qr_HO2 = ar_HO2 = 6.59f-3


# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Threshold Energies in eV: Single Float32 for SPD and SPI, 2D Tuple for DPD, DPI, DiPI (1st bond energy, 2nd bond energy)
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const tsh_HO2 = (2.82f0,) # "HO2 -> OH + O"

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Species Names 1D Tuple with 3 elements for SPD, SPI, 2D Tuple with 3 elements for DPD, DPI, DiPI
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const sn_HO2 = (("HO2", "OH", "O"),)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Reaction probabilities: Every Tuple must contain as many elements as the number of wavelength ranges
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const rp_HO2 = ((1.00f0),)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Reaction Identifiers: Custom, for posterior analysis
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const rn_HO2 = ("HO2-PD1",)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Reaction Types: SPD / SPI / DPD / DPI / DiPI
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const rt_HO2 = ("SPD",)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Wavelength Ranges
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

const wv_HO2 = ((0f0, 4395.0f0),)

# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
# Build Struct
# ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────

HO2 = Species(
    wvl_thr_HO2,
    qr_HO2,
    qr_HO2,
    tsh_HO2,
    sn_HO2,
    rp_HO2,
    rn_HO2,
    rt_HO2,
    wv_HO2
)